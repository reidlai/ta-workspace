package main

import (
	"context"
	"flag"
	"fmt"
	stdlog "log"
	"net"
	"net/url"
	"os"
	"os/signal"
	"sync"
	"syscall"

	// Internal Modules
	"example.com/taassistant/modules/insights"
	"example.com/taassistant/modules/watchlist"

	// Generated Interfaces
	// For backward compatibility if needed, or remove
	insightsGen "example.com/taassistant/api/gen/insights"
	watchlistGen "example.com/taassistant/api/gen/watchlist"

	"goa.design/clue/debug"
	"goa.design/clue/log"
)

func main() {
	// Define command line flags, add any other flag required to configure the
	// service.
	var (
		hostF     = flag.String("host", "localhost", "Server host (valid values: localhost)")
		domainF   = flag.String("domain", "", "Host domain name (overrides host domain specified in service design)")
		httpPortF = flag.String("http-port", "", "HTTP port (overrides host HTTP port specified in service design)")
		secureF   = flag.Bool("secure", false, "Use secure scheme (https or grpcs)")
		dbgF      = flag.Bool("debug", false, "Log request and response bodies")
	)
	flag.Parse()

	// Setup logger. Replace logger with your own log package of choice.
	format := log.FormatJSON
	if log.IsTerminal() {
		format = log.FormatTerminal
	}
	ctx := log.Context(context.Background(), log.WithFormat(format))
	if *dbgF {
		ctx = log.Context(ctx, log.WithDebug())
		log.Debugf(ctx, "debug logs enabled")
	}
	log.Print(ctx, log.KV{K: "http-port", V: *httpPortF})

	// Initialize the services.
	// Initialize the services.
	var (
		watchlistSvc watchlistGen.Service
		insightsSvc  insightsGen.Service
	)
	{
		stdLogger := stdlog.New(os.Stderr, "[taassistant] ", stdlog.LstdFlags)
		watchlistSvc = watchlist.NewWatchlist(stdLogger)
		insightsSvc = insights.NewInsights(stdLogger)
	}

	// Wrap the services in endpoints that can be invoked from other services
	// potentially running in different processes.
	var (
		watchlistEndpoints *watchlistGen.Endpoints
		insightsEndpoints  *insightsGen.Endpoints
	)
	{
		watchlistEndpoints = watchlistGen.NewEndpoints(watchlistSvc)
		watchlistEndpoints.Use(debug.LogPayloads())
		watchlistEndpoints.Use(log.Endpoint)
		insightsEndpoints = insightsGen.NewEndpoints(insightsSvc)
		insightsEndpoints.Use(debug.LogPayloads())
		insightsEndpoints.Use(log.Endpoint)
	}

	// Create channel used by both the signal handler and server goroutines
	// to notify the main goroutine when to stop the server.
	errc := make(chan error)

	// Setup interrupt handler. This optional step configures the process so
	// that SIGINT and SIGTERM signals cause the services to stop gracefully.
	go func() {
		c := make(chan os.Signal, 1)
		signal.Notify(c, syscall.SIGINT, syscall.SIGTERM)
		errc <- fmt.Errorf("%s", <-c)
	}()

	var wg sync.WaitGroup
	ctx, cancel := context.WithCancel(ctx)

	// Start the servers and send errors (if any) to the error channel.
	switch *hostF {
	case "localhost":
		{
			addr := "http://localhost:8080"
			u, err := url.Parse(addr)
			if err != nil {
				log.Fatalf(ctx, err, "invalid URL %#v\n", addr)
			}
			if *secureF {
				u.Scheme = "https"
			}
			if *domainF != "" {
				u.Host = *domainF
			}
			if *httpPortF != "" {
				h, _, err := net.SplitHostPort(u.Host)
				if err != nil {
					log.Fatalf(ctx, err, "invalid URL %#v\n", u.Host)
				}
				u.Host = net.JoinHostPort(h, *httpPortF)
			} else if u.Port() == "" {
				u.Host = net.JoinHostPort(u.Host, "80")
			}
			handleHTTPServer(ctx, u, watchlistEndpoints, insightsEndpoints, &wg, errc, *dbgF)
		}

	default:
		log.Fatal(ctx, fmt.Errorf("invalid host argument: %q (valid hosts: localhost)", *hostF))
	}

	// Wait for signal.
	log.Printf(ctx, "exiting (%v)", <-errc)

	// Send cancellation signal to the goroutines.
	cancel()

	wg.Wait()
	log.Printf(ctx, "exited")
}
